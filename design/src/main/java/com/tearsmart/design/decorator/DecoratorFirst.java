public class DecoratorFirst extends AbstractDecorator {
    public DecoratorFirst() {
        System.out.println("DecoratorFirst np-args");
    }

    DecoratorFirst(Human human) {
        super(human);
        System.out.println("DecoratorFirst");
    }

    public void goHome() {
        System.out.println("进房子。。");
    }

    public void findMap() {
        System.out.println("书房找找Map。。");
    }

    @Override
    public void wearClothes() {
        // TODO Auto-generated method stub
        super.wearClothes();
        goHome();
    }

    @Override
    public void walkToWhere() {
        // TODO Auto-generated method stub
        super.walkToWhere();
        findMap();
    }
}
